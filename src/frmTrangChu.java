    private void btnThem_HangHoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThem_HangHoaActionPerformed
        // TODO add your handling code here:
        ResultSet rs = daoHangHoa.getIDLoai((String)cbbLoaiHangHoa_HangHoa.getSelectedItem());
        int idLoai = 0;
        try {
            while(rs.next())
            {
                idLoai = rs.getInt(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmTrangChu.class.getName()).log(Level.SEVERE, null, ex);
        }
        try
        {
            HangHoa data = new HangHoa(txtTen_HangHoa.getText(), idLoai, Integer.parseInt(txtSoLuong_HangHoa.getText()),txtDonViTinh_HangHoa.getText(), Double.parseDouble(txtGiaNhap_HangHoa.getText()),Double.parseDouble(txtGiaXuat_HangHoa.getText()));
            daoHangHoa.InsertData(data);
            loadDataHangHoa();
            resttext();
            JOptionPane.showMessageDialog(this,"Thêm thành công");
        }catch(Exception ex)
        {
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnThem_HangHoaActionPerformed

